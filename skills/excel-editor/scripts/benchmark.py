"""
Excel Automation Benchmark — 2-backend, 3-size comparison

Backends:
  1. pywin32  — Python COM (cross-process IPC per property)        [persistent session]
  2. vba      — Python + VBA Application.Run (in-process, zero IPC) [persistent session]

Document sizes (fixtures generated by gen_fixture.py):
  empty   1 sheet, no data
  medium  3 sheets x 1000 x 50  = 150K cells
  large   10 sheets x 20000 x 100 = 20M cells, ~145 MB

Usage:
  python benchmark.py --all --size empty,medium,large
  python benchmark.py --backends pywin32,vba --size large

Output: per-size Markdown tables (with ms units) + JSON results file.
"""
import argparse
import json
import os
import shutil
import sys
import tempfile
import time

try:
    sys.stdout.reconfigure(encoding="utf-8", errors="replace")
    sys.stderr.reconfigure(encoding="utf-8", errors="replace")
except Exception:
    pass

# Backends that keep the workbook open for the whole session (open once, many ops).
PERSISTENT_BACKENDS = ("pywin32", "vba")

SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))


# ---------------------------------------------------------------------------
# COM Backend runner (pywin32 / VBA)
# ---------------------------------------------------------------------------

class COMBackend:
    """Drives pywin32 or VBA backend via the excel_editor.py engine (stateful session)."""

    def __init__(self, name, visible=False):
        self.name = name
        self.visible = visible
        self.engine = None

    def init_app(self):
        """Start the Excel application only (no workbook opened yet)."""
        sys.path.insert(0, SCRIPT_DIR)
        from excel_editor import ExcelCOM, ExcelVBA
        if self.name == "vba":
            self.engine = ExcelVBA(visible=self.visible)
        else:
            self.engine = ExcelCOM(visible=self.visible)

    def stop(self):
        if self.engine:
            try:
                self.engine.close()
            except Exception:
                pass
            self.engine = None


# ---------------------------------------------------------------------------
# Fixtures
# ---------------------------------------------------------------------------

FIXTURES_DIR = os.path.abspath(os.path.join(SCRIPT_DIR, "..", "..", "..", "benchmarks", "fixtures"))


def fixture_path(size):
    """Return the source fixture path for a size, or None for the empty tier."""
    if size == "empty":
        return None
    return os.path.join(FIXTURES_DIR, f"bench_{size}.xlsx")


def prepare_working_file(workdir, name, size):
    """Copy the size's fixture to a fresh per-round working file.

    Returns (filepath, exists) — exists is True when a fixture was copied in
    (so an 'open' can be timed); False for the empty tier (file created by the
    first op). The copy itself is intentionally untimed.
    """
    filepath = os.path.join(workdir, f"work_{name}_{size}.xlsx")
    if os.path.isfile(filepath):
        os.remove(filepath)
    src = fixture_path(size)
    if src is None:
        return filepath, False
    if not os.path.isfile(src):
        raise FileNotFoundError(
            f"fixture missing: {src}\n  generate it first: python gen_fixture.py --size {size}")
    shutil.copy2(src, filepath)
    return filepath, True


# ---------------------------------------------------------------------------
# Benchmark tasks — shared op parameters
# ---------------------------------------------------------------------------

def gen_bulk_data(rows=100, cols=10):
    return [[f"R{r}C{c}" if c == 0 else r * cols + c for c in range(cols)] for r in range(rows)]


# Non-overlapping single-row 3-col ranges for the merge test (clear of B:K / M used elsewhere).
MERGE_RANGES = [f"AA{r}:AC{r}" for r in range(1, 6)]


def run_benchmark_com(backend, workdir, size):
    """Run benchmarks for a COM-based backend (pywin32/vba) at a given size."""
    results = {}
    filepath, has_file = prepare_working_file(workdir, backend.name, size)

    backend.init_app()
    engine = backend.engine
    is_vba = backend.name == "vba"

    try:
        # B0: open speed (time opening the fixture). Empty tier has no file to open.
        if has_file:
            t0 = time.perf_counter()
            engine.open(filepath)
            results["B0_open"] = {"ops": 1, "total_ms": round((time.perf_counter() - t0) * 1000, 1), "ok": True}
        else:
            engine.create(filepath)  # setup, untimed
            results["B0_open"] = {"note": "N/A (no file)"}

        # B1: cell.write (5 cells)
        test_values = [("auto", "Hello"), ("auto", 42), ("auto", 3.14159),
                       ("auto", True), ("formula", "=1+1")]
        times = []
        for i, (kind, value) in enumerate(test_values):
            t0 = time.perf_counter()
            engine.write_cell("Sheet1", f"A{i+1}", value, kind)
            times.append((time.perf_counter() - t0) * 1000)
        results["B1_cell_write"] = _agg(times)

        # B2: write_range (100x10)
        bulk_data = gen_bulk_data(100, 10)
        t0 = time.perf_counter()
        if is_vba:
            engine.execute_actions([{"action": "write_range", "sheet": "Sheet1", "range": "B1:K100", "values": bulk_data}])
        else:
            engine.write_range("Sheet1", "B1:K100", tuple(tuple(r) for r in bulk_data))
        results["B2_write_bulk_100x10"] = {"ops": 1, "total_ms": round((time.perf_counter() - t0) * 1000, 1), "ok": True}

        # B3: read cell
        t0 = time.perf_counter()
        if is_vba:
            engine.execute_actions([{"action": "read_cell", "sheet": "Sheet1", "cell": "A1"}])
        else:
            engine.read_cell("Sheet1", "A1")
        results["B3_range_read"] = {"ops": 1, "total_ms": round((time.perf_counter() - t0) * 1000, 1), "ok": True}

        # B4: clear range
        t0 = time.perf_counter()
        if is_vba:
            engine.execute_actions([{"action": "clear_range", "sheet": "Sheet1", "range": "B1:K100"}])
        else:
            engine.clear_range("Sheet1", "B1:K100")
        results["B4_range_clear"] = {"ops": 1, "total_ms": round((time.perf_counter() - t0) * 1000, 1), "ok": True}

        # B5: inspect
        t0 = time.perf_counter()
        engine.inspect()
        results["B5_inspect"] = {"ops": 1, "total_ms": round((time.perf_counter() - t0) * 1000, 1), "ok": True}

        # B6: batch 10 writes
        t0 = time.perf_counter()
        if is_vba:
            engine.execute_actions([
                {"action": "write_cell", "sheet": "Sheet1", "cell": f"M{i+1}", "value": i * 100}
                for i in range(10)])
        else:
            for i in range(10):
                engine.write_cell("Sheet1", f"M{i+1}", i * 100)
        results["B6_batch_10_writes"] = {"ops": 10, "total_ms": round((time.perf_counter() - t0) * 1000, 1),
                                         "avg_ms": round((time.perf_counter() - t0) * 1000 / 10, 1)}

        # B7: set_format (5 cells)
        t0 = time.perf_counter()
        if is_vba:
            engine.execute_actions([
                {"action": "set_format", "sheet": "Sheet1", "range": f"A{i+1}",
                 "bold": True, "font_size": 14, "bg_color": 65535} for i in range(5)])
        else:
            for i in range(5):
                engine.set_format("Sheet1", f"A{i+1}", bold=True, font_size=14, bg_color=65535)
        results["B7_set_format"] = {"ops": 5, "total_ms": round((time.perf_counter() - t0) * 1000, 1)}

        # B8: insert rows
        t0 = time.perf_counter()
        if is_vba:
            engine.execute_actions([{"action": "insert_rows", "sheet": "Sheet1", "row": 1, "count": 5}])
        else:
            engine.insert_rows("Sheet1", 1, 5)
        results["B8_insert_rows"] = {"ops": 1, "total_ms": round((time.perf_counter() - t0) * 1000, 1), "ok": True}

        # B9: sheet ops (add + rename + delete)
        t0 = time.perf_counter()
        if is_vba:
            engine.execute_actions([
                {"action": "add_sheet", "name": "BenchSheet"},
                {"action": "rename_sheet", "sheet": "BenchSheet", "new_name": "Renamed"},
                {"action": "delete_sheet", "sheet": "Renamed"}])
        else:
            engine.add_sheet("BenchSheet")
            engine.rename_sheet("BenchSheet", "Renamed")
            engine.delete_sheet("Renamed")
        results["B9_sheet_ops"] = {"ops": 3, "total_ms": round((time.perf_counter() - t0) * 1000, 1)}

        # B10: merge (5 non-overlapping ranges)
        t0 = time.perf_counter()
        if is_vba:
            engine.execute_actions([{"action": "merge_cells", "sheet": "Sheet1", "range": rng} for rng in MERGE_RANGES])
        else:
            for rng in MERGE_RANGES:
                engine.merge_cells("Sheet1", rng)
        results["B10_merge"] = {"ops": 5, "total_ms": round((time.perf_counter() - t0) * 1000, 1)}

        engine.save()
    finally:
        backend.stop()
        if os.path.isfile(filepath):
            os.remove(filepath)

    return results


def _agg(times):
    """Aggregate a list of per-op ms into a result dict."""
    return {
        "ops": len(times),
        "total_ms": round(sum(times), 1),
        "avg_ms": round(sum(times) / len(times), 1) if times else 0,
    }



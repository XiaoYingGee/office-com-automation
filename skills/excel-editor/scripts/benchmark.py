def average_rounds(round_results):
    """Average measured rounds, preserving notes and ops counts."""
    averaged = {}
    for key in round_results[0]:
        first = round_results[0][key]
        if "note" in first:
            averaged[key] = {"note": first["note"]}
            continue
        totals = [r[key].get("total_ms", 0) for r in round_results if key in r and "total_ms" in r[key]]
        ops_list = [r[key].get("ops", 0) for r in round_results if key in r]
        if not totals:
            averaged[key] = dict(first)
            continue
        avg_total = sum(totals) / len(totals)
        avg_ops = round(sum(ops_list) / len(ops_list)) if ops_list else 0
        entry = {"ops": avg_ops, "total_ms": round(avg_total, 1)}
        if avg_ops > 1 and avg_total > 0:
            entry["avg_ms"] = round(avg_total / avg_ops, 1)
        averaged[key] = entry
    return averaged


def main():
    parser = argparse.ArgumentParser(description="Excel Automation Benchmark")
    parser.add_argument("--all", action="store_true", help="Run all available backends")
    parser.add_argument("--backends", default="pywin32,vba",
                        help="Comma-separated backends (pywin32,vba)")
    parser.add_argument("--size", default="empty",
                        help="Comma-separated document sizes (empty,medium,large)")
    parser.add_argument("--headed", action="store_true", help="Show Excel window")
    parser.add_argument("--output-dir", default=None, help="Output directory for results")
    parser.add_argument("--warmup", type=int, default=1, help="Warmup rounds (default 1)")
    parser.add_argument("--rounds", type=int, default=3, help="Measurement rounds (default 3)")
    args = parser.parse_args()

    backends_to_run = ["pywin32", "vba"] if args.all \
        else [b.strip() for b in args.backends.split(",") if b.strip()]
    sizes = [s.strip() for s in args.size.split(",") if s.strip()]

    output_dir = args.output_dir or os.path.join(SCRIPT_DIR, "..", "..", "..", "benchmarks", "results")
    output_dir = os.path.abspath(output_dir)
    os.makedirs(output_dir, exist_ok=True)

    workdir = tempfile.mkdtemp(prefix="excel_bench_")
    print(f"Workdir: {workdir}")
    print(f"Backends: {backends_to_run}")
    print(f"Sizes: {sizes}")
    print(f"Rounds: {args.warmup} warmup + {args.rounds} measured\n")

    all_results = {}

    for size in sizes:
        all_results[size] = {}
        for bname in backends_to_run:
            print(f"{'='*60}\n  {size} / {bname}\n{'='*60}")

            if bname not in PERSISTENT_BACKENDS:
                print(f"  [skip] Unknown backend: {bname}")
                continue

            round_results = []
            for r in range(args.warmup + args.rounds):
                is_warmup = r < args.warmup
                label = f"warmup {r+1}" if is_warmup else f"round {r+1-args.warmup}"
                print(f"  [{bname}] {label}...", end=" ", flush=True)
                try:
                    backend = COMBackend(bname, visible=args.headed)
                    result = run_benchmark_com(backend, workdir, size)
                    print("done")
                    if not is_warmup:
                        round_results.append(result)
                except Exception as e:
                    print(f"ERROR: {e}")
                    continue

            if round_results:
                all_results[size][bname] = average_rounds(round_results)
            else:
                print(f"  [{bname}] No successful rounds at {size}!")
                all_results[size][bname] = {k: {"note": "FAILED"} for k, _ in BENCHMARKS}

    generate_report(all_results, output_dir, sizes)

    try:
        shutil.rmtree(workdir, ignore_errors=True)
    except Exception:
        pass


if __name__ == "__main__":
    main()

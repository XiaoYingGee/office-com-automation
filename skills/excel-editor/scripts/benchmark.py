# Report Generation
# ---------------------------------------------------------------------------

def _cell(data):
    """Render one result cell with units."""
    if not data:
        return "—"
    if data.get("note"):
        return data["note"]
    ms = data.get("total_ms")
    if isinstance(ms, (int, float)) and ms > 0:
        return f"{ms:.0f} ms"
    return "—"


def generate_report(all_results, output_dir, sizes):
    """all_results: {size: {backend: {test: data}}}. One latency table per size."""
    lines = []
    lines.append("# Excel Automation Benchmark Results\n")
    lines.append(f"> Date: {time.strftime('%Y-%m-%d %H:%M')}")
    lines.append(f"> Sizes: {', '.join(sizes)}")
    lines.append("")

    for size in sizes:
        size_res = all_results.get(size, {})
        if not size_res:
            continue
        backends = list(size_res.keys())
        lines.append(f"## Size: {size}  (latency, lower = faster)\n")
        lines.append("| Benchmark | " + " | ".join(backends) + " |")
        lines.append("|-----------|" + "|".join(["------:" for _ in backends]) + "|")
        for key, label in BENCHMARKS:
            row = f"| {label} |"
            for b in backends:
                row += f" {_cell(size_res[b].get(key, {}))} |"
            lines.append(row)
        lines.append("")

    lines.append("## Notes\n")
    lines.append("- **pywin32**: Python -> cross-process COM IPC (one call per property), persistent session.")
    lines.append("- **vba**: Python -> Application.Run -> in-process VBA (zero IPC), persistent session.")
    lines.append("- `B0 Open` is N/A for the empty tier (no file to open).")

    report_md = "\n".join(lines)
    os.makedirs(output_dir, exist_ok=True)
    md_path = os.path.join(output_dir, "benchmark-results.md")
    json_path = os.path.join(output_dir, "benchmark-results.json")
    with open(md_path, "w", encoding="utf-8") as f:
        f.write(report_md)
    with open(json_path, "w", encoding="utf-8") as f:
        json.dump(all_results, f, indent=2, ensure_ascii=False)

    print(f"\nResults written to:\n  {md_path}\n  {json_path}")
    print(f"\n{report_md}")


